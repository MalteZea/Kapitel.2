public class Variabler {
    public static void main(String[] args) {
        byte first = 5;
        short second = 10000;
        int third = 4999390;
        long fourth = 110100101010010001l;
        float decimals = 1.422442f;
        double bigDecimals = 1.982838838838;
        boolean one = true;
        char a = 'a';
        String car = "Red";

        long multiply = first + second - third + fourth;

        System.out.println(multiply);
        System.out.println(decimals + bigDecimals);
        System.out.println(one);
        System.out.println(a);
        System.out.println(car);


    }
}
